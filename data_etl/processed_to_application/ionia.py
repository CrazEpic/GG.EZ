def get_top_3_pros(role: str, player_scores):
    role_players = pros_ionia_metrics.get(role, {})
    if role not in pros_ionia_metrics:
        # just do all roles
        role_players = {}
        for _role in pros_ionia_metrics:
            for player_name, metrics in pros_ionia_metrics[_role].items():
                role_players[player_name] = metrics
    top_players = []

    def cosine_similarity(a, b):
        return np.dot(a, b) / (norm(a) * norm(b))

    player_vector = np.array(
        [
            player_scores["combat_effectiveness"],
            player_scores["team_contribution"],
            player_scores["resource_efficiency"],
            player_scores["vision_awareness"],
            player_scores["consistency"],
        ]
    )
    for player_name, metrics in role_players.items():
        pro_vector = np.array(
            [
                metrics["combat_effectiveness"],
                metrics["team_contribution"],
                metrics["resource_efficiency"],
                metrics["vision_awareness"],
                metrics["consistency"],
            ]
        )
        similarity = cosine_similarity(player_vector, pro_vector).item()
        pro_archetype = metrics["archetype_id"]
        top_players.append((player_name, similarity, pro_archetype))
    top_players.sort(key=lambda x: x[1], reverse=True)
    return top_players[:3]


def determine_archetype(player_scores):
    def cosine_similarity(a, b):
        return np.dot(a, b) / (norm(a) * norm(b))

    archetype_means_vectors = np.array(
        [
            [
                archetype["combat_effectiveness"],
                archetype["team_contribution"],
                archetype["resource_efficiency"],
                archetype["vision_awareness"],
                archetype["consistency"],
            ]
            for archetype in archetype_means
        ]
    )
    player_vector = np.array(
        [
            player_scores["combat_effectiveness"],
            player_scores["team_contribution"],
            player_scores["resource_efficiency"],
            player_scores["vision_awareness"],
            player_scores["consistency"],
        ]
    )

    similarities = [cosine_similarity(player_vector, a) for a in archetype_means_vectors]
    assigned = np.argmax(similarities).item()
    return {
        "archetype": assigned,
        "similarity_score": similarities[assigned].item(),
    }


